#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
EXPERIMENT_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"

RUN_ID="${1:-$(date -u +%Y%m%dT%H%M%SZ)}"
ISSUE_COUNT="${ISSUE_COUNT:-10}"
CONTEXT_LINES="${CONTEXT_LINES:-180}"

RUN_DIR="$EXPERIMENT_DIR/runs/$RUN_ID"
WORKSPACE="$RUN_DIR/workspace"
HANDOFF_PATH="/private/tmp/ralph-context-handoff-$RUN_ID.md"

if [[ -e "$RUN_DIR" ]]; then
  echo "Run directory already exists: $RUN_DIR" >&2
  exit 1
fi

mkdir -p "$WORKSPACE"/{fixtures,observations}

render_template() {
  local src="$1"
  local dest="$2"
  sed \
    -e "s|{{RUN_ID}}|$RUN_ID|g" \
    -e "s|{{WORKSPACE}}|$WORKSPACE|g" \
    "$src" > "$dest"
}

render_template "$EXPERIMENT_DIR/STARTER_PROMPT.template.md" "$RUN_DIR/STARTER_PROMPT.md"
render_template "$EXPERIMENT_DIR/STARTER_PROMPT_NO_BUDGET.template.md" "$RUN_DIR/STARTER_PROMPT_NO_BUDGET.md"
render_template "$EXPERIMENT_DIR/EXPECTED_RESULTS.template.md" "$RUN_DIR/EXPECTED_RESULTS.md"

cat > "$WORKSPACE/AGENTS.md" <<EOF_AGENTS
# Ralph Context Handoff Drill Workspace

This is a disposable local-only experiment generated by skillmonger.

Rules:
- Do not push to any remote.
- Commits are local observation artifacts.
- Use bd for issue tracking.
- The expected handoff path is: $HANDOFF_PATH
- The experiment passes only if Ralph writes the handoff before built-in chat compaction is needed.
EOF_AGENTS

cat > "$WORKSPACE/.gitignore" <<'EOF_GITIGNORE'
observations/
EOF_GITIGNORE

cat > "$WORKSPACE/check.sh" <<'EOF_CHECK'
#!/usr/bin/env bash
set -euo pipefail

if [[ $# -ne 1 ]]; then
  echo "usage: ./check.sh <issue-number>" >&2
  exit 2
fi

issue_number="$1"
expected="issue-${issue_number} complete"

if [[ ! -f results.log ]]; then
  echo "FAIL missing results.log"
  exit 1
fi

if grep -Fxq "$expected" results.log; then
  echo "PASS $expected"
else
  echo "FAIL missing: $expected"
  exit 1
fi
EOF_CHECK
chmod +x "$WORKSPACE/check.sh"

cd "$WORKSPACE"
git init -q
git config user.name "Ralph Context Drill"
git config user.email "ralph-context-drill@example.invalid"

bd init --non-interactive --prefix RCH --skip-agents >/dev/null

for i in $(seq 1 "$ISSUE_COUNT"); do
  issue_num="$(printf "%02d" "$i")"
  fixture="fixtures/issue-$issue_num-context.md"
  body="fixtures/issue-$issue_num-body.md"

  {
    echo "# Fixture for issue $issue_num"
    echo
    echo "Marker phrase: fixture-$issue_num-alpha"
    echo "Marker phrase: fixture-$issue_num-beta"
    echo "Marker phrase: fixture-$issue_num-gamma"
    echo
    for line in $(seq 1 "$CONTEXT_LINES"); do
      printf 'Context line %03d for issue %s: this deliberately repetitive text exists to consume context while remaining deterministic for observation.\n' "$line" "$issue_num"
    done
  } > "$fixture"

  {
    echo "# Context drill issue $issue_num"
    echo
    echo "This issue is intentionally verbose. Ralph should read this full issue and the referenced fixture to create predictable context pressure."
    echo
    echo "Referenced fixture: \`$fixture\`"
    echo
    echo "Required result line: \`issue-$issue_num complete\`"
    echo
    echo "Acceptance workflow:"
    echo
    echo "1. Read this full issue body."
    echo "2. Read the full referenced fixture."
    echo "3. Append exactly \`issue-$issue_num complete\` to \`results.log\`."
    echo "4. Run \`./check.sh $issue_num\`."
    echo "5. QA must verify only the issue-local result and avoid unrelated changes."
    echo
    for line in $(seq 1 "$CONTEXT_LINES"); do
      printf 'Issue body filler %03d for issue %s: deterministic context-pressure text for the Ralph handoff drill.\n' "$line" "$issue_num"
    done
  } > "$body"

  bd create "Context handoff drill issue $issue_num" \
    --type task \
    --priority P2 \
    --labels "ralph-context-drill,dummy" \
    --body-file "$body" \
    --acceptance "Append 'issue-$issue_num complete' to results.log and pass ./check.sh $issue_num." \
    --notes "Generated by run $RUN_ID. Expected handoff path: $HANDOFF_PATH" \
    --silent >/dev/null
done

bd export -o .beads/issues.jsonl >/dev/null
git add .gitignore AGENTS.md check.sh fixtures .beads/issues.jsonl
git commit -q -m "Set up Ralph context handoff drill"

cat > "$RUN_DIR/RUN_INFO.env" <<EOF_INFO
RUN_ID=$RUN_ID
RUN_DIR=$RUN_DIR
WORKSPACE=$WORKSPACE
HANDOFF_PATH=$HANDOFF_PATH
ISSUE_COUNT=$ISSUE_COUNT
CONTEXT_LINES=$CONTEXT_LINES
EOF_INFO

echo "Created run: $RUN_DIR"
echo "Workspace:   $WORKSPACE"
echo "Prompt:      $RUN_DIR/STARTER_PROMPT.md"
echo "No-budget:   $RUN_DIR/STARTER_PROMPT_NO_BUDGET.md"
echo "Handoff:     $HANDOFF_PATH"
